package org.wecancodeit.pairgenerater;

public class Student {

	private String name;
	private String key;
	
	public String getName() {
		return name;
	}

	public String getKey() {
		return key;
		
	}
	
	public Student(String name, String key) {
		super();
		this.name = name;
		this.key = key;
	}


}
